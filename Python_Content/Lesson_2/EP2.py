# Simple math operators (+, -, *, /) Addition, Subtraction, Multiplication, Division
# Assigning variables



# Assigning variables
number1 = 20
number2 = 6


# Performing math operations
sum_result = number1 + number2       # Addition
difference = number1 - number2       # Subtraction
product = number1 * number2          # Multiplication
quotient = round(float(number1 / number2),3)        # Division

# float = 4.5
# Integers = whole # ex: 5

# Displaying the results
print(sum_result)       # Outputs 20
print("Subtraction:", difference)    # Outputs 10
print("Multiplication:", product)    # Outputs 75
print("Division:", quotient)         # Outputs 3.0

